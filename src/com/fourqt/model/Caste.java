package com.fourqt.model;

public class Caste {
	
	private String Caste_Category_Id;
	private String Caste_Id;
	private String Caste_Name;
	public String getCaste_Category_Id() {
		return Caste_Category_Id;
	}
	public void setCaste_Category_Id(String caste_Category_Id) {
		Caste_Category_Id = caste_Category_Id;
	}
	public String getCaste_Id() {
		return Caste_Id;
	}
	public void setCaste_Id(String caste_Id) {
		Caste_Id = caste_Id;
	}
	public String getCaste_Name() {
		return Caste_Name;
	}
	public void setCaste_Name(String caste_Name) {
		Caste_Name = caste_Name;
	}

}
