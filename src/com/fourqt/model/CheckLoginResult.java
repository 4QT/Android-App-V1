package com.fourqt.model;

public class CheckLoginResult {
	
	private String BookingApproval;
	private String EmpName;
	private String Lead;
	private String LoginID;
	public String getBookingApproval() {
		return BookingApproval;
	}
	public void setBookingApproval(String bookingApproval) {
		BookingApproval = bookingApproval;
	}
	public String getEmpName() {
		return EmpName;
	}
	public void setEmpName(String empName) {
		EmpName = empName;
	}
	public String getLead() {
		return Lead;
	}
	public void setLead(String lead) {
		Lead = lead;
	}
	public String getLoginID() {
		return LoginID;
	}
	public void setLoginID(String loginID) {
		LoginID = loginID;
	}
	

}
