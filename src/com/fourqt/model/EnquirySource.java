package com.fourqt.model;

public class EnquirySource {
	
	private String EnquirySourceName;
	private String SourceID;
	private String Source_Category_Id;
	public String getEnquirySourceName() {
		return EnquirySourceName;
	}
	public void setEnquirySourceName(String enquirySourceName) {
		EnquirySourceName = enquirySourceName;
	}
	public String getSourceID() {
		return SourceID;
	}
	public void setSourceID(String sourceID) {
		SourceID = sourceID;
	}
	public String getSource_Category_Id() {
		return Source_Category_Id;
	}
	public void setSource_Category_Id(String source_Category_Id) {
		Source_Category_Id = source_Category_Id;
	}

}
