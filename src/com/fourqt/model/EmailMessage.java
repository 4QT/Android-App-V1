package com.fourqt.model;

public class EmailMessage {
	
	private String CC;
	private String CommaSeparatedFileNames;
	private String Enquiry_Id;
	private String Login_Id;
	private String MessageBody;
	private String Subject;
	private String Subject_Id;
	public String getCC() {
		return CC;
	}
	public void setCC(String cC) {
		CC = cC;
	}
	public String getCommaSeparatedFileNames() {
		return CommaSeparatedFileNames;
	}
	public void setCommaSeparatedFileNames(String commaSeparatedFileNames) {
		CommaSeparatedFileNames = commaSeparatedFileNames;
	}
	public String getEnquiry_Id() {
		return Enquiry_Id;
	}
	public void setEnquiry_Id(String enquiry_Id) {
		Enquiry_Id = enquiry_Id;
	}
	public String getLogin_Id() {
		return Login_Id;
	}
	public void setLogin_Id(String login_Id) {
		Login_Id = login_Id;
	}
	public String getMessageBody() {
		return MessageBody;
	}
	public void setMessageBody(String messageBody) {
		MessageBody = messageBody;
	}
	public String getSubject() {
		return Subject;
	}
	public void setSubject(String subject) {
		Subject = subject;
	}
	public String getSubject_Id() {
		return Subject_Id;
	}
	public void setSubject_Id(String subject_Id) {
		Subject_Id = subject_Id;
	}
	

}
