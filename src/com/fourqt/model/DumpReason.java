package com.fourqt.model;

public class DumpReason {
	
	private String DumpID;
	private String DumpName;
	public String getDumpID() {
		return DumpID;
	}
	public void setDumpID(String dumpID) {
		DumpID = dumpID;
	}
	public String getDumpName() {
		return DumpName;
	}
	public void setDumpName(String dumpName) {
		DumpName = dumpName;
	}

}
