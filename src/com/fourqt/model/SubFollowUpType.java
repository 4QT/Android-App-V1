package com.fourqt.model;

public class SubFollowUpType {

	private String FollowupCategoryID;
	private String FollowupType;
	private String FollowupTypeID;
	private String MainFollowupTypeID;
	public String getFollowupCategoryID() {
		return FollowupCategoryID;
	}
	public void setFollowupCategoryID(String followupCategoryID) {
		FollowupCategoryID = followupCategoryID;
	}
	public String getFollowupType() {
		return FollowupType;
	}
	public void setFollowupType(String followupType) {
		FollowupType = followupType;
	}
	public String getFollowupTypeID() {
		return FollowupTypeID;
	}
	public void setFollowupTypeID(String followupTypeID) {
		FollowupTypeID = followupTypeID;
	}
	public String getMainFollowupTypeID() {
		return MainFollowupTypeID;
	}
	public void setMainFollowupTypeID(String mainFollowupTypeID) {
		MainFollowupTypeID = mainFollowupTypeID;
	}
}
